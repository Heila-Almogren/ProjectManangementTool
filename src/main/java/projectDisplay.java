    public int getTotalCost(){
        int total = 0;
        for (Task task : MainClass.selected_project.tasks) {
            total += task.Cost;
            
        }
        return total;
    }
    
    public Object[][] getData(){
        Object[][] all_tasks = new Object[MainClass.selected_project.tasks.size()][5];
        System.out.println(MainClass.selected_project.tasks.size());
        int counter = 0;
        for (Task task : MainClass.selected_project.tasks) {
            
            
                all_tasks[counter][0]= task.title;
                all_tasks[counter][1]= task.Start_Date;
                all_tasks[counter][2]= task.Duration;
                all_tasks[counter][3]= task.Assigned_Resources;
                all_tasks[counter][4]= task.Cost;

                counter++;
      }
        
        return all_tasks;
    }

